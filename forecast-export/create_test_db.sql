-- Drop tables
DROP TABLE FORECASTING_HIST;
Drop table forecasting;
DROP TABLE ITEM;
DROP TABLE WAREHOUSE;

-- Create schema
CREATE TABLE WAREHOUSE
(ID NUMBER(4) PRIMARY KEY,
WAREHOUSE_CODE VARCHAR2(32) NOT NULL);

CREATE TABLE ITEM
(ID NUMBER(4) PRIMARY KEY,
ITEM_CODE VARCHAR(32) NOT NULL,
UNIT_COST NUMBER(8,2) NOT NULL,
CURRENCY VARCHAR(32) NOT NULL,
WAREHOUSE_ID  NUMBER(4) NOT NULL REFERENCES WAREHOUSE(ID),
ACTIVE VARCHAR2(1) NOT NULL
);

CREATE TABLE FORECASTING
(ID NUMBER(4) PRIMARY KEY REFERENCES ITEM(ID),
TOT_FORECAST NUMBER(8,4) NOT NULL,
EOD NUMBER(8,4) NOT NULL,
STD_DEVIATION NUMBER(8,4) NOT NULL,
TREND NUMBER(8,4) NOT NULL);

CREATE TABLE FORECASTING_HIST
(ID NUMBER(4) PRIMARY KEY,
ITEM_ID NUMBER(4) NOT NULL REFERENCES ITEM(ID),
TOT_FORECAST NUMBER(8,4) NOT NULL,
FORECAST_DATE VARCHAR2(8) NOT NULL);

CREATE UNIQUE INDEX FH_ITEM_ID_FORECAST_DATE ON FORECASTING_HIST(ITEM_ID,FORECAST_DATE);

